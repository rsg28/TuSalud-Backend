-- SEDE LIMA: mismos precios que Arequipa para los exámenes de este script (solo los que aún no tienen precio en Lima)
INSERT INTO examen_precio (examen_id, sede_id, precio)
SELECT ep.examen_id, @sede_lima, ep.precio
FROM examen_precio ep
WHERE ep.sede_id = @sede_arequipa
  AND NOT EXISTS (SELECT 1 FROM examen_precio x WHERE x.sede_id = @sede_lima AND x.examen_id = ep.examen_id);

-- SEDE CUSCO: mismos precios que Arequipa para los exámenes de este script (solo los que aún no tienen precio en Cusco)
INSERT INTO examen_precio (examen_id, sede_id, precio)
SELECT ep.examen_id, @sede_cusco, ep.precio
FROM examen_precio ep
WHERE ep.sede_id = @sede_arequipa
  AND NOT EXISTS (SELECT 1 FROM examen_precio x WHERE x.sede_id = @sede_cusco AND x.examen_id = ep.examen_id);

