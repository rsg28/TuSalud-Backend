-- =============================================================================
SELECT 'Total Exámenes de LABORATORIO:' AS Info, COUNT(*) AS Total FROM examenes WHERE categoria = 'LABORATORIO';
SELECT 'Total Precios LABORATORIO por sede:' AS Info, s.nombre AS Sede, COUNT(ep.id) AS Total_Precios
FROM sedes s
LEFT JOIN examen_precio ep ON s.id = ep.sede_id
LEFT JOIN examenes e ON ep.examen_id = e.id
WHERE e.categoria = 'LABORATORIO'
GROUP BY s.id, s.nombre;
